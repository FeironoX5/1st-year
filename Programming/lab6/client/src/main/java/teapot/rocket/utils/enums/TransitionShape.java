package teapot.rocket.utils.enums;

/**
 * Represents the shape of a transition component used in rocket body
 * assemblies.
 *
 * <p>
 * Each enum constant describes the specific shape of the transition component,
 * distinguishing between conical with straight sides or conical with rounded
 * sides.
 * </p>
 *
 * <p>
 * This enum is used to specify and manage different transition shapes within
 * the rocket body assembly system.
 * </p>
 *
 * @author Gleb Kiva
 */
public enum TransitionShape {

    /**
     * A conical transition shape with straight sides.
     */
    CONICAL,

    /**
     * An ellipsoidal transition shape with rounded sides.
     */
    ELLIPSOID
}
